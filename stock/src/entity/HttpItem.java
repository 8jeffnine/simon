package entity;

public class HttpItem {
	
	private String compcd, method, accept, a_encoding, a_lang, cache, host, referer, origin, proxy, upgrade, x_requested, cookie;

	public String getCompcd() {
		return compcd;
	}

	public void setCompcd(String compcd) {
		this.compcd = compcd;
	}

	public String getMethod() {
		return method;
	}

	public void setMethod(String method) {
		this.method = method;
	}

	public String getAccept() {
		return accept;
	}

	public void setAccept(String accept) {
		this.accept = accept;
	}

	public String getA_encoding() {
		return a_encoding;
	}

	public void setA_encoding(String a_encoding) {
		this.a_encoding = a_encoding;
	}

	public String getA_lang() {
		return a_lang;
	}

	public void setA_lang(String a_lang) {
		this.a_lang = a_lang;
	}

	public String getCache() {
		return cache;
	}

	public void setCache(String cache) {
		this.cache = cache;
	}

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public String getReferer() {
		return referer;
	}

	public void setReferer(String referer) {
		this.referer = referer;
	}

	public String getOrigin() {
		return origin;
	}

	public void setOrigin(String origin) {
		this.origin = origin;
	}

	public String getProxy() {
		return proxy;
	}

	public void setProxy(String proxy) {
		this.proxy = proxy;
	}

	public String getUpgrade() {
		return upgrade;
	}

	public void setUpgrade(String upgrade) {
		this.upgrade = upgrade;
	}

	public String getX_requested() {
		return x_requested;
	}

	public void setX_requested(String x_requested) {
		this.x_requested = x_requested;
	}

	public String getCookie() {
		return cookie;
	}

	public void setCookie(String cookie) {
		this.cookie = cookie;
	}
	

}
