package entity;

public class News {

	private String pubDate, title, content, press, artiURL, keyword;
	private int cnt, seq, pagef;
	private String titlesec, titlethi;
	
	public String getPubDate() {
		return pubDate;
	}
	public void setPubDate(String pubDate) {
		this.pubDate = pubDate;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getPress() {
		return press;
	}
	public void setPress(String press) {
		this.press = press;
	}
	public String getArtiURL() {
		return artiURL;
	}
	public void setArtiURL(String artiURL) {
		this.artiURL = artiURL;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
	public int getPagef() {
		return pagef;
	}
	public void setPagef(int pagef) {
		this.pagef = pagef;
	}
	public String getTitlesec() {
		return titlesec;
	}
	public void setTitlesec(String titlesec) {
		this.titlesec = titlesec;
	}
	public String getTitlethi() {
		return titlethi;
	}
	public void setTitlethi(String titlethi) {
		this.titlethi = titlethi;
	}

}
